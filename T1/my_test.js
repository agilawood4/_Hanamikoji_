// T1 自动化测试：hanamikoji_judge
import { hanamikoji_judge } from "./t1-as/build/release.js";

// ─── 测试框架 ────────────────────────────────────────────────
let passed = 0;
let failed = 0;

function check(desc, board, round, expected) {
  const result = hanamikoji_judge(Int8Array.from(board), round);
  if (result === expected) {
    console.log(`  ✅ PASS  ${desc}`);
    passed++;
  } else {
    console.log(`  ❌ FAIL  ${desc}`);
    console.log(`           board=${JSON.stringify(board)}  round=${round}`);
    console.log(`           expected=${expected}  got=${result}`);
    failed++;
  }
}

// ─── 分类1：一方分值达到 11 分，立即获胜 ────────────────────
console.log("\n【分类1】一方分值达到 11 分，立即获胜");

// 我方 G(5)+F(4)+D(3)=12 ≥ 11
check("我方 G+F+D=12≥11，第1轮",
  [0, 0, 0, 1, 0, 1, 1], 1, 1);

// 我方 G(5)+F(4)+C(2)=11（恰好11）
check("我方 G+F+C=11（恰好），第2轮",
  [0, 0, 1, 0, 0, 1, 1], 2, 1);

// 对手 G(5)+F(4)+E(3)=12 ≥ 11
check("对手 G+F+E=12≥11，第2轮",
  [0, 0, 0, 0, -1, -1, -1], 2, -1);

// 对手 G(5)+F(4)+A(2)=11，第3轮
check("对手 G+F+A=11，第3轮",
  [-1, 0, 0, 0, 0, -1, -1], 3, -1);

// 我方11分且对手也达到11分 → 我方11分先判，我方赢
// 我方 G(5)+F(4)+C(2)=11；对手 D(3)+E(3)+A(2)+B(2)+... 凑不到11，换个例子
// 我方 G(5)+F(4)+D(3)=12；对手 E(3)+A(2)+B(2)+C(2)... 凑11：E+A+B+C+F? 不行
// 实际上两方同时≥11很难，但代码逻辑是先判我方
check("我方≥11分，先于对手判定，我方赢",
  [1, 1, 0, 1, 0, 1, 1], 1, 1);

// ─── 分类2：一方获得至少 4 枚倾心标记获胜 ───────────────────
console.log("\n【分类2】一方获得 ≥4 枚倾心标记获胜");

// 我方4枚（A+B+C+D=2+2+2+3=9<11），对手分值<11
check("我方4枚A+B+C+D，对手未≥11，第1轮",
  [1, 1, 1, 1, 0, 0, 0], 1, 1);

// 我方5枚，对手未≥11
check("我方5枚，对手未≥11，第2轮",
  [1, 1, 0, 1, 1, 0, 1], 2, 1);

// 对手4枚，我方未≥11
check("对手4枚，我方未≥11，第1轮",
  [-1, -1, -1, -1, 0, 0, 0], 1, -1);

// 我方4枚但对手已≥11 → 对手赢（11分条件优先于4枚条件）
// 我方4枚A+B+C+D=9，对手G+F+E=12≥11
check("我方4枚但对手≥11分，对手赢",
  [1, 1, 1, 1, -1, -1, -1], 2, -1);

// 对手4枚但我方已≥11 → 我方赢
check("对手4枚但我方≥11分，我方赢",
  [-1, -1, -1, -1, 1, 1, 1], 3, 1);

// ─── 分类3：前两小轮未满足胜利条件，返回 0 ──────────────────
console.log("\n【分类3】前两小轮未满足胜利条件，返回 0（继续游戏）");

check("全部中立，第1轮",
  [0, 0, 0, 0, 0, 0, 0], 1, 0);

// 我方3枚A+B+C=6<11，枚数不足4
check("我方3枚（6分），第1轮",
  [1, 1, 1, 0, 0, 0, 0], 1, 0);

// 对手3枚G+F+E=12 → 等等这已经≥11了，换：对手G+D+E=11→对手赢
// 对手3枚G(5)+E(3)+A(2)=10<11，枚数不足4
check("对手3枚（10分），第2轮",
  [-1, 0, 0, 0, -1, 0, -1], 2, 0);

// 双方各2枚，均不满足
check("我方2枚对手2枚，第2轮",
  [1, 0, -1, 0, 1, 0, -1], 2, 0);

// 我方G+F=9分，不足11，枚数2不足4
check("我方G+F=9分，第1轮",
  [0, 0, 0, 0, 0, 1, 1], 1, 0);

// ─── 分类4：第三小轮，总分不同，高者获胜 ───────────────────
console.log("\n【分类4】第三小轮结束，总分不同，高者获胜");

// 我方G(5)+D(3)=8，对手F(4)+C(2)=6
check("第3轮：我方8分>对手6分，我方赢",
  [0, 0, -1, 1, 0, -1, 1], 3, 1);

// 我方B(2)=2，对手G(5)+E(3)=8
check("第3轮：我方2分<对手8分，对手赢",
  [0, 1, 0, 0, -1, 0, -1], 3, -1);

// 我方A+B+C+D=9，对手G+E=8
check("第3轮：我方9分>对手8分，我方赢",
  [1, 1, 1, 1, -1, 0, -1], 3, 1);

// 对手F(4)+D(3)+B(2)=9，我方E(3)+A(2)=5
check("第3轮：对手9分>我方5分，对手赢",
  [1, -1, 0, -1, 1, -1, 0], 3, -1);

// ─── 分类5：第三小轮，总分相同，最高档标记决胜 ─────────────
console.log("\n【分类5】第三小轮总分相同，最高档倾心标记决胜");

// 我方G(5)+A(2)=7，对手F(4)+D(3)=7，分同
// G档：我方有G → 我方赢
check("第3轮分同(7-7)，G档我方有，我方赢",
  [1, 0, 0, -1, 0, -1, 1], 3, 1);

// 我方F(4)+D(3)=7，对手G(5)+A(2)=7，分同
// G档：对手有G → 对手赢
check("第3轮分同(7-7)，G档对手有，对手赢",
  [-1, 0, 0, 1, 0, 1, -1], 3, -1);

// 双方无G：我方F(4)+A(2)+B(2)=8，对手E(3)+D(3)+C(2)=8，分同
// G档：均无；F档：我方有F → 我方赢
check("第3轮分同(8-8)，无G，F档我方有，我方赢",
  [1, 1, -1, -1, -1, 1, 0], 3, 1);

// 双方无G无F：我方D(3)+E(3)=6，对手A(2)+B(2)+C(2)=6，分同
// G/F均无；DE档：我方有D和E，对手无 → 我方赢
check("第3轮分同(6-6)，无G无F，DE档我方有对手无，我方赢",
  [0, 0, -1, 1, 1, 0, 0], 3, 1);

// 双方无G无F无DE：我方A(2)+B(2)+C(2)=6，对手...
// 但A/B/C各只有1枚标记，我有A+B+C=6，对手没有标记 → 应该是我方4枚→上面已测
// 换：我方A+B=4，对手C+D... 分同需要对手4分，对手C(2)+A？不行
// 我方C(2)+D(3)=5，对手B(2)+E(3)=5，无G无F
// DE档：我有D，对手有E → 都有继续；ABC档：我有C，对手有B → 都有→平局
// 但这是分类6的内容，这里测"ABC档一方有一方无"：
// 我方A(2)+D(3)=5，对手E(3)+B(2)=5，DE都有，ABC：我有A对手有B都有→平局，跳过
// 我方A(2)+C(2)+B(2)=6，对手D(3)+E(3)=6，无G无F，
// DE：对手有D和E，我方无 → 对手赢
check("第3轮分同(6-6)，无G无F，DE档对手有我方无，对手赢",
  [0, 0, 0, -1, -1, 0, 0], 3, -1);

// ─── 分类6：第三小轮平局，返回 2 ───────────────────────────
console.log("\n【分类6】第三小轮平局，返回 2");

// 全部中立，第3轮
check("全部中立，第3轮，平局",
  [0, 0, 0, 0, 0, 0, 0], 3, 2);

// 我方C(2)+D(3)=5，对手B(2)+E(3)=5，无G无F
// DE：我有D，对手有E → 都有继续；ABC：我有C，对手有B → 都有 → 平局
check("第3轮5-5，无G无F，DE各一，ABC各一，平局",
  [0, -1, 1, 1, -1, 0, 0], 3, 2);

// 我方A(2)+D(3)=5，对手B(2)+E(3)=5，无G无F
// DE：都有；ABC：都有 → 平局
check("第3轮5-5，我方A+D，对手B+E，平局",
  [1, -1, 0, 1, -1, 0, 0], 3, 2);

// ─── 额外边界测试 ────────────────────────────────────────────
console.log("\n【额外】边界与特殊情况");

// 我方全取7枚（21分），第1轮 → 我方赢
check("我方全取7枚21分，第1轮",
  [1, 1, 1, 1, 1, 1, 1], 1, 1);

// 对手全取7枚，第2轮 → 对手赢
check("对手全取7枚，第2轮",
  [-1, -1, -1, -1, -1, -1, -1], 2, -1);

// 我方G+F=9分、2枚，第3轮（不满足立即胜利），分更高 → 我方赢（分类4）
check("第3轮我方G+F=9，对手0分，我方赢",
  [0, 0, 0, 0, 0, 1, 1], 3, 1);

// 只有G=0中立，其余我方有，第3轮
// 我方A+B+C+D+E+F=2+2+2+3+3+4=16≥11且6枚≥4 → 我方赢
check("我方6枚共16分，第3轮",
  [1, 1, 1, 1, 1, 1, 0], 3, 1);

// ─── 汇总 ────────────────────────────────────────────────────
console.log(`\n========================================`);
console.log(`结果：${passed} 通过，${failed} 失败，共 ${passed + failed} 项`);
if (failed === 0) {
  console.log("🎉 You have passed all the tests provided.");
} else {
  console.log("⚠️  部分测试未通过，请检查实现。");
  process.exit(1);
}